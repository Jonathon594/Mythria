    private static void registerCollectionWeight(final Item[] items, float value) {
        for (Item item : items) {
            itemWeightMap.put(item, value);
        }
    }

    private static void registerCollectionWeight(final Block[] blocks, float value) {
        for (Block block : blocks) {
            Item item = MythriaUtil.getItemFromItemOrBlock(block);
            itemWeightMap.put(item, value);
        }
    }

    public static double getWeight(ItemStack is) {
        double weight = 0;
        if (is.getItem() instanceof IItemData) {
            IItemData id = (IItemData) is.getItem();
            weight = id.getWeight();
        } else {
            if (!itemWeightMap.containsKey(is.getItem()))
                return 0;
            final double iw = itemWeightMap.get(is.getItem()) * is.getCount();
            weight = iw;
        }
        return weight;
    }
